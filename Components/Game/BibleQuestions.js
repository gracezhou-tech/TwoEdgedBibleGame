export function getRandomQuestions(count = 7) {
  const shuffled = [...bibleQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function checkAnswer(userAnswer, question) {
  const normalizedUser = userAnswer.toLowerCase().trim();
  return question.acceptableAnswers.some(
    accepted => normalizedUser === accepted.toLowerCase() ||
    normalizedUser.includes(accepted.toLowerCase()) ||
    accepted.toLowerCase().includes(normalizedUser)
  );
}
